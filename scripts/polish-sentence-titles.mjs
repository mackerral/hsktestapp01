const path = "HSKList/sentences-hsk1-3.txt";
const lines = readFileSync(path, "utf8").replace(/\r\n/g, "\n").split("\n");
const out = [];
let title = "";
let count = 0;
let extrasQueued = [];

for (let i = 0; i < lines.length; i++) {
  const line = lines[i];
  const trim = line.trim();

  if (trim.startsWith("@title")) {
    const raw = trim.replace("@title", "").trim();
    const zh = raw.includes("|") ? raw.split("|")[0].trim() : raw;
    title = zh;
    count = 0;
    const th = TITLE_TH[zh] || "";
    out.push(th ? `@title ${zh}|${th}` : `@title ${zh}`);
    extrasQueued = EXTRA[zh] ? [...EXTRA[zh]] : [];
    continue;
  }

  if (trim.startsWith("@card") || trim.startsWith("@level") || trim.startsWith("#") || trim === "") {
    // flush extras before leaving card? handled at next @card/@level
    if ((trim.startsWith("@card") || trim.startsWith("@level")) && extrasQueued.length && count < 8) {
      while (extrasQueued.length && count < 8) {
        out.push(extrasQueued.shift());
        count++;
      }
    }
    out.push(line);
    if (trim.startsWith("@card") || trim.startsWith("@level")) {
      title = "";
      count = 0;
      extrasQueued = [];
    }
    continue;
  }

  if (title && trim && !trim.startsWith("@")) {
    out.push(line);
    count++;
    continue;
  }

  out.push(line);
}

// flush last card extras
if (extrasQueued.length && count < 8) {
  while (extrasQueued.length && count < 8) {
    out.push(extrasQueued.shift());
    count++;
  }
}

// fix header
let text = out.join("\n");
text = text.replace(
  /# HSK 1–3 sentence stories\. Max 6 sentences per card\./,
  "# HSK 1–3 sentence stories. Max 8 sentences per card.",
);
text = text.replace(
  /ไม่เกิน 6 ประโยค/g,
  "ไม่เกิน 8 ประโยค",
);

writeFileSync(path, text);
