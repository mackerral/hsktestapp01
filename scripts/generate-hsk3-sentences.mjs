function formatCard([titleZh, titleTh, ...sents]) {
  const lines = sents.slice(0, 8);
  return ["@card", `@title ${titleZh}|${titleTh}`, ...lines].join("\n");
}

const path = "HSKList/sentences-hsk1-3.txt";
let content = readFileSync(path, "utf8").replace(/\r\n/g, "\n");
const idx = content.indexOf("@level 3");
if (idx < 0) throw new Error("no @level 3");
const body = ["@level 3", ...CARDS.map(formatCard)].join("\n") + "\n";
content = content.slice(0, idx) + body;
content = content.replace(
  /# HSK 1: 300\/300 unique\. HSK 2: 200\/200 unique \(HSK1–2 vocab only\)\./,
  "# HSK 1: 300/300 · HSK 2: 200/200 · HSK 3: 500/500 (HSK1–3 only).",
);
writeFileSync(path, content);
